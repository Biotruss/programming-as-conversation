#Enter examples in irb to see return values!!

#puts prints a string to the screen and creates a "newline" because it adds an invisible newline(\n) character at the end of the string.
puts "hi"

#print is the same as puts, but does not have the invisible newline(\n) character at the end.
print "hi"

#p is also the same as puts, but it allows a return value. Which is useful beacause as you know statements return a nil value otherwise. It is also designed for debugging as it shows a more "raw" version of an object.
p "Hello World"
 => "Hello World"
